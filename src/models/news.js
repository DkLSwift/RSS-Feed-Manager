
export class News {
    constructor(title, link, pubDate, author, description, url, imgUrl, guid, source, baseUrl) {

        this.title = title
        this.link = link
        this.pubDate = pubDate
        this.author = author
        this.description = description
        this.url = url
        this.imgUrl = imgUrl
        this.guid = guid
        this.source = source
        this.baseUrl = baseUrl
    }
}

